<?php


class Book
{
    private $id;
    private $title;


    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }

    public function getTitle()
    {
        return $this->title;
    }

    public function setTitle($title)
    {
        $this->title = $title;
    }

    public function getTitleUpperCase(){
        return strtoupper($this->title);
    }

}